/* Difference between TypeScript and JavaScript */

/* 
    Typescript is a superset of javascript    
 
   -> TypeScript is known as an Object-oriented programming language 
   -> whereas JavaScript is a prototype-based language.

   -> TypeScript has a feature known as Static typing 
   -> JavaScript does not support this feature

   -> TypeScript supports Interfaces but JavaScript does not.

    -> Error can be found during compile-time
    -> Error can be found  only  during run-time


*/